class Airline {
    enum `Type`: String {
        case fullservice = "fullservice"
        case lowcost = "lowcost"
        case cargo = "cargo"
    }
    
    enum Country: String {
        case CHN = "China"
        case HKG = "Hong Kong"
        case MAC = "Macau"
        case TWN = "Taiwan"
    }
    
    var name: String
    var type: Type
    var country: Country
    var iataCode: String
    var icaoCode: String
    var hub: [String]
    var focus: [String]
    var founded: Int
    var callsign: String
    var fleetSize: Int
    var website: String
    var description: String
    
    init(name: String, type: Type, country: Country, iataCode: String, icaoCode: String, hub: [String], focus: [String], founded: Int, callsign: String, fleetSize: Int, website: String, description: String) {
        self.name = name
        self.type = type
        self.country = country
        self.iataCode = iataCode
        self.icaoCode = icaoCode
        self.hub = hub
        self.focus = focus
        self.founded = founded
        self.callsign = callsign
        self.fleetSize = fleetSize
        self.website = website
        self.description = description
    }
    
}
